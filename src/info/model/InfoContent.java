package info.model;

public class InfoContent {
	private Integer number;
	private String content;
	
	
	public InfoContent(Integer number, String content) {
		super();
		this.number = number;
		this.content = content;
	}


	public Integer getNumber() {
		return number;
	}


	public String getContent() {
		return content;
	}
	
	
}
