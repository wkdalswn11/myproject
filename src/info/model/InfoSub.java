package info.model;

public class InfoSub {
	private int id;
	private int good;
	private int hate;
	
	
	
	
	
	
	public InfoSub(int id, int good, int hate) {
		super();
		this.id = id;
		this.good = good;
		this.hate = hate;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGood() {
		return good;
	}
	public void setGood(int good) {
		this.good = good;
	}
	public int getHate() {
		return hate;
	}
	public void setHate(int hate) {
		this.hate = hate;
	}
	
	
	
}
