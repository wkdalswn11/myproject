package snack.model;

public class SnackContent {
	private Integer number;
	private String content;
	
	public SnackContent(Integer number, String content) {
		super();
		this.number = number;
		this.content = content;
	}

	public Integer getNumber() {
		return number;
	}

	public String getContent() {
		return content;
	}
	
	
	
	
}
